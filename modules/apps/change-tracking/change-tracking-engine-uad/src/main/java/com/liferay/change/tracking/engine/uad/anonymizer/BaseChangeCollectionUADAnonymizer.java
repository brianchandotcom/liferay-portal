package com.liferay.change.tracking.engine.uad.anonymizer;

import com.liferay.change.tracking.engine.model.ChangeCollection;
import com.liferay.change.tracking.engine.service.ChangeCollectionLocalService;
import com.liferay.change.tracking.engine.uad.constants.ChangeTrackingEngineUADConstants;

import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;

import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the change collection UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link ChangeCollectionUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseChangeCollectionUADAnonymizer
	extends DynamicQueryUADAnonymizer<ChangeCollection> {
	@Override
	public void autoAnonymize(ChangeCollection changeCollection, long userId,
		User anonymousUser) throws PortalException {
		if (changeCollection.getUserId() == userId) {
			changeCollection.setUserId(anonymousUser.getUserId());
			changeCollection.setUserName(anonymousUser.getFullName());
		}

		changeCollectionLocalService.updateChangeCollection(changeCollection);
	}

	@Override
	public void delete(ChangeCollection changeCollection)
		throws PortalException {
		changeCollectionLocalService.deleteChangeCollection(changeCollection);
	}

	@Override
	public Class<ChangeCollection> getTypeClass() {
		return ChangeCollection.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return changeCollectionLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return ChangeTrackingEngineUADConstants.USER_ID_FIELD_NAMES_CHANGE_COLLECTION;
	}

	@Reference
	protected ChangeCollectionLocalService changeCollectionLocalService;
}