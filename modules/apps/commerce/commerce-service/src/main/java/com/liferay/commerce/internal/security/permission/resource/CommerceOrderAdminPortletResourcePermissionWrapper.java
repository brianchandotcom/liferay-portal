package com.liferay.commerce.internal.security.permission.resource;

import com.liferay.commerce.constants.CommercePortletKeys;
import com.liferay.portal.kernel.security.permission.resource.BasePortletResourcePermissionWrapper;
import com.liferay.portal.kernel.security.permission.resource.PortletResourcePermission;
import com.liferay.portal.kernel.security.permission.resource.PortletResourcePermissionFactory;

import org.osgi.service.component.annotations.Component;

/**
 * @author Alessio Antonio Rendina
 */
@Component(
	property = "resource.name=" + CommercePortletKeys.COMMERCE_ORDER,
	service = PortletResourcePermission.class
)
public class CommerceOrderAdminPortletResourcePermissionWrapper
	extends BasePortletResourcePermissionWrapper {

	@Override
	protected PortletResourcePermission doGetPortletResourcePermission() {
		return PortletResourcePermissionFactory.create(
			CommercePortletKeys.COMMERCE_ORDER,
			new CommerceOrderPortletResourcePermissionLogic());
	}

}