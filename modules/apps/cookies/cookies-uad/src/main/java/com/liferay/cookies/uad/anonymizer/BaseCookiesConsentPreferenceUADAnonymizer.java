package com.liferay.cookies.uad.anonymizer;

import com.liferay.asset.kernel.model.AssetEntry;
import com.liferay.asset.kernel.service.AssetEntryLocalService;
import com.liferay.cookies.model.CookiesConsentPreference;
import com.liferay.cookies.service.CookiesConsentPreferenceLocalService;
import com.liferay.cookies.uad.constants.CookiesUADConstants;
import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;
import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the cookies consent preference UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link CookiesConsentPreferenceUADAnonymizer}.
 * </p>
 *
 * @author Christopher Kian
 * @generated
 */
public abstract class BaseCookiesConsentPreferenceUADAnonymizer
	extends DynamicQueryUADAnonymizer<CookiesConsentPreference> {

	@Override
	public void autoAnonymize(
			CookiesConsentPreference cookiesConsentPreference, long userId,
			User anonymousUser)
		throws PortalException {

		if (cookiesConsentPreference.getUserId() == userId) {
			cookiesConsentPreference.setUserId(anonymousUser.getUserId());
			cookiesConsentPreference.setUserName(anonymousUser.getFullName());

			autoAnonymizeAssetEntry(cookiesConsentPreference, anonymousUser);
		}

		cookiesConsentPreferenceLocalService.updateCookiesConsentPreference(
			cookiesConsentPreference);
	}

	@Override
	public void delete(CookiesConsentPreference cookiesConsentPreference)
		throws PortalException {

		cookiesConsentPreferenceLocalService.deleteCookiesConsentPreference(
			cookiesConsentPreference);
	}

	@Override
	public Class<CookiesConsentPreference> getTypeClass() {
		return CookiesConsentPreference.class;
	}

	protected void autoAnonymizeAssetEntry(
		CookiesConsentPreference cookiesConsentPreference, User anonymousUser) {

		AssetEntry assetEntry = fetchAssetEntry(cookiesConsentPreference);

		if (assetEntry != null) {
			assetEntry.setUserId(anonymousUser.getUserId());
			assetEntry.setUserName(anonymousUser.getFullName());

			assetEntryLocalService.updateAssetEntry(assetEntry);
		}
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return cookiesConsentPreferenceLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return CookiesUADConstants.
			USER_ID_FIELD_NAMES_COOKIES_CONSENT_PREFERENCE;
	}

	protected AssetEntry fetchAssetEntry(
		CookiesConsentPreference cookiesConsentPreference) {

		return assetEntryLocalService.fetchEntry(
			CookiesConsentPreference.class.getName(),
			cookiesConsentPreference.getCookiesConsentPreferenceId());
	}

	@Reference
	protected AssetEntryLocalService assetEntryLocalService;

	@Reference
	protected CookiesConsentPreferenceLocalService
		cookiesConsentPreferenceLocalService;

}
