/**
 * Provides the base implementation for the dataset view state entry UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link DatasetViewStateEntryUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseDatasetViewStateEntryUADAnonymizer
	extends DynamicQueryUADAnonymizer<DatasetViewStateEntry> {

	@Override
	public void autoAnonymize(
			DatasetViewStateEntry datasetViewStateEntry, long userId,
			User anonymousUser)
		throws PortalException {

		if (datasetViewStateEntry.getUserId() == userId) {
			datasetViewStateEntry.setUserId(anonymousUser.getUserId());
			datasetViewStateEntry.setUserName(anonymousUser.getFullName());
		}

		datasetViewStateEntryLocalService.updateDatasetViewStateEntry(
			datasetViewStateEntry);
	}

	@Override
	public void delete(DatasetViewStateEntry datasetViewStateEntry)
		throws PortalException {

		datasetViewStateEntryLocalService.deleteDatasetViewStateEntry(
			datasetViewStateEntry);
	}

	@Override
	public Class<DatasetViewStateEntry> getTypeClass() {
		return DatasetViewStateEntry.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return datasetViewStateEntryLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return DatasetViewUADConstants.
			USER_ID_FIELD_NAMES_DATASET_VIEW_STATE_ENTRY;
	}

	@Reference
	protected DatasetViewStateEntryLocalService
		datasetViewStateEntryLocalService;

}