package com.liferay.fragment.renderer.collection.filter.internal;

import com.liferay.osgi.service.tracker.collections.map.ServiceTrackerMap;
import com.liferay.osgi.service.tracker.collections.map.ServiceTrackerMapFactory;
import com.liferay.portal.kernel.log.Log;
import com.liferay.portal.kernel.log.LogFactoryUtil;
import org.osgi.framework.BundleContext;
import org.osgi.service.component.annotations.Activate;
import org.osgi.service.component.annotations.Component;
import org.osgi.service.component.annotations.Deactivate;

/**
 * @author Pablo Molina
 */
@Component(immediate = true, service = FormFieldRendererTrackerImpl.class)
public class FormFieldRendererTrackerImpl {

	public FormFieldRenderer getFormFieldRenderer(String infoItemType) {
		return _serviceTrackerMap.getService(infoItemType);
	}

	@Activate
	protected void activate(BundleContext bundleContext) {
		_serviceTrackerMap = ServiceTrackerMapFactory.openSingleValueMap(
			bundleContext, FormFieldRenderer.class, null,
			(serviceReference, emitter) ->
				emitter.emit
					((String) serviceReference.getProperty("info.field.type")));
	}

	@Deactivate
	protected void deactivate() {
		_serviceTrackerMap.close();
	}

	private static final Log _log = LogFactoryUtil.getLog(
		FormFieldRendererTrackerImpl.class);

	private ServiceTrackerMap<String, FormFieldRenderer>
		_serviceTrackerMap;

}