package com.liferay.frontend.view.state.uad.anonymizer;

import com.liferay.frontend.view.state.model.FrontendViewStateEntry;
import com.liferay.frontend.view.state.service.FrontendViewStateEntryLocalService;
import com.liferay.frontend.view.state.uad.constants.FrontendViewStateUADConstants;
import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;
import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the frontend view state entry UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link FrontendViewStateEntryUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseFrontendViewStateEntryUADAnonymizer
	extends DynamicQueryUADAnonymizer<FrontendViewStateEntry> {

	@Override
	public void autoAnonymize(
			FrontendViewStateEntry frontendViewStateEntry, long userId,
			User anonymousUser)
		throws PortalException {

		if (frontendViewStateEntry.getUserId() == userId) {
			frontendViewStateEntry.setUserId(anonymousUser.getUserId());
			frontendViewStateEntry.setUserName(anonymousUser.getFullName());
		}

		frontendViewStateEntryLocalService.updateFrontendViewStateEntry(
			frontendViewStateEntry);
	}

	@Override
	public void delete(FrontendViewStateEntry frontendViewStateEntry)
		throws PortalException {

		frontendViewStateEntryLocalService.deleteFrontendViewStateEntry(
			frontendViewStateEntry);
	}

	@Override
	public Class<FrontendViewStateEntry> getTypeClass() {
		return FrontendViewStateEntry.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return frontendViewStateEntryLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return FrontendViewStateUADConstants.
			USER_ID_FIELD_NAMES_FRONTEND_VIEW_STATE_ENTRY;
	}

	@Reference
	protected FrontendViewStateEntryLocalService
		frontendViewStateEntryLocalService;

}