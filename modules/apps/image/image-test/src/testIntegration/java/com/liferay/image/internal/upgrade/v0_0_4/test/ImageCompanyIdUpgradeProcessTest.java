/**
 * @author Georgel Pop
 */
@RunWith(Arquillian.class)
public class ImageCompanyIdUpgradeProcessTest {

	@ClassRule
	@Rule
	public static final AggregateTestRule aggregateTestRule =
		new AggregateTestRule(
			new LiferayIntegrationTestRule(),
			PermissionCheckerMethodTestRule.INSTANCE);

	@Before
	public void setUp() throws Exception {
		_group = GroupTestUtil.addGroup();
	}

	@Test
	public void testUpgrade() throws Exception {
		Layout layout = LayoutTestUtil.addTypePortletLayout(_group);

		layout = _layoutLocalService.updateIconImage(
			layout.getPlid(),
			FileUtil.getBytes(getClass(), "dependencies/liferay.jpg"));

		Image image = _imageLocalService.getImage(layout.getIconImageId());

		image.setCompanyId(RandomTestUtil.randomLong());

		image = _imageLocalService.updateImage(image);

		_runUpgrade();

		Image expectedImage = _imageLocalService.getImage(image.getImageId());

		Assert.assertEquals(
			expectedImage.getCompanyId(), layout.getCompanyId());
	}

	private void _runUpgrade() throws Exception {
		UpgradeProcess[] upgradeProcesses = UpgradeTestUtil.getUpgradeSteps(
			_upgradeStepRegistrator, Version.parseVersion("0.0.4"));

		UpgradeProcess upgradeProcess = upgradeProcesses[0];

		upgradeProcess.upgrade();

		_entityCache.clearCache();
		_multiVMPool.clear();
	}

	@Inject(
		filter = "(&(component.name=com.liferay.image.internal.upgrade.registry.ImageServiceUpgradeStepRegistrator))"
	)
	private static UpgradeStepRegistrator _upgradeStepRegistrator;

	@Inject
	private EntityCache _entityCache;

	@DeleteAfterTestRun
	private Group _group;

	@Inject
	private ImageLocalService _imageLocalService;

	@Inject
	private LayoutLocalService _layoutLocalService;

	@Inject
	private MultiVMPool _multiVMPool;

}