package com.liferay.remote.web.app.uad.anonymizer;

import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;
import com.liferay.remote.web.app.model.RemoteWebAppEntry;
import com.liferay.remote.web.app.service.RemoteWebAppEntryLocalService;
import com.liferay.remote.web.app.uad.constants.RemoteWebAppUADConstants;
import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the remote web app entry UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link RemoteWebAppEntryUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseRemoteWebAppEntryUADAnonymizer
	extends DynamicQueryUADAnonymizer<RemoteWebAppEntry> {

	@Override
	public void autoAnonymize(
			RemoteWebAppEntry remoteWebAppEntry, long userId,
			User anonymousUser)
		throws PortalException {

		if (remoteWebAppEntry.getUserId() == userId) {
			remoteWebAppEntry.setUserId(anonymousUser.getUserId());
			remoteWebAppEntry.setUserName(anonymousUser.getFullName());
		}

		remoteWebAppEntryLocalService.updateRemoteWebAppEntry(
			remoteWebAppEntry);
	}

	@Override
	public void delete(RemoteWebAppEntry remoteWebAppEntry)
		throws PortalException {

		remoteWebAppEntryLocalService.deleteRemoteWebAppEntry(
			remoteWebAppEntry);
	}

	@Override
	public Class<RemoteWebAppEntry> getTypeClass() {
		return RemoteWebAppEntry.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return remoteWebAppEntryLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return RemoteWebAppUADConstants.
			USER_ID_FIELD_NAMES_REMOTE_WEB_APP_ENTRY;
	}

	@Reference
	protected RemoteWebAppEntryLocalService remoteWebAppEntryLocalService;

}