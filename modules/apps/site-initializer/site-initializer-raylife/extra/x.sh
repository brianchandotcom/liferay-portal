function write_craco_config_js {
	cat <<EOF > craco.config.js
const sassRegex = /\.(scss|sass)$/;

module.exports = {
	webpack: {
		configure: (webpackConfig) => {
			/**
			 * This change is necessary to import SCSS as string
			 * to inject into style tag
			 */

			webpackConfig.module.rules[1].oneOf.unshift({
				exclude: /node_modules/,
				test: sassRegex,
				use: [
					'sass-to-string',
					{
						loader: 'sass-loader',
						options: {
							sassOptions: {
								outputStyle: 'compressed',
							},
						},
					},
				],
			});

			/**
			 * Avoid hashes in filenames
			 */

			webpackConfig.output.chunkFilename = 'static/js/[name].js';
			webpackConfig.output.filename = 'static/js/[name].js';

			return webpackConfig;
		},
	},
};
EOF
}

function write_gitignore {
	cat <<EOF > .gitignore
EOF
}

function write_index_js {
cat <<EOF > index.js
import React from 'react';
import ReactDOM from 'react-dom';

import {WebComponent} from './common/WebComponent';
import SharedStyle from './common/styles/index.scss';
import HelloWorld from './routes/hello-world/pages/HelloWorld';

class HelloWorldWebComponent extends WebComponent {
	connectedCallback() {
		super.connectedCallback(SharedStyle);

		ReactDOM.render(
			<HelloWorld />,
			this.mountPoint
		);
	}
}

const ELEMENT_ID = 'liferay-${CUSTOM_ELEMENT_NAME}';

if (!customElements.get(ELEMENT_ID)) {
	customElements.define(ELEMENT_ID, HelloWorldWebComponent);
}
EOF
}


function remove_unnecessary_files {

	#
	# All the files deleted here, are generated by Create React App
	#

	rm -f App* index* setupTests.js logo.svg reportWebVitals.js
	rm -f ../public/logo* ../public/manifest.json ../public/favicon.ico ../public/robots.txt
}

function write_common_files {
	#
	# Generate base Web Component
	#

	cat <<EOF > common/WebComponent.js
export class WebComponent extends HTMLElement {
	constructor() {
		super();
		this.styleSass = document.createElement('style');
		this.mountPoint = document.createElement('div');

		this.attachShadow({mode: 'open'});
	}

	connectedCallback(StylesProvider) {
		this.styleSass.textContent = StylesProvider;
		this.shadowRoot.appendChild(this.styleSass);
		this.shadowRoot.appendChild(this.mountPoint);
	}
}
EOF

	#
	# Generating Liferay API
	#

	cat <<EOF > common/services/liferay/api.js
const {REACT_APP_LIFERAY_API = window.location.origin} = process.env;

export const getLiferayAuthenticationToken = () => {
	try {
		// eslint-disable-next-line no-undef
		const token = Liferay.authToken;

		return token;
	} catch (error) {
		console.warn('Not able to find Liferay auth token\n', error);

		return '';
	}
};

const baseFetch = async (url, {body, method = 'GET'} = {}) => {
	const response = await fetch(REACT_APP_LIFERAY_API + '/' + url, {
		...(body && {body: JSON.stringify(body)}),
		headers: {
			'Content-Type': 'application/json',
			'x-csrf-token': getLiferayAuthenticationToken(),
		},
		method,
	});

	const data = await response.json();

	return {data};
};

export {REACT_APP_LIFERAY_API};

export default baseFetch;

EOF
	
	#
	# Generating Styles
	#

	cat <<EOF > common/styles/variables.scss
\$primary-color: #295ccc;
EOF

	cat <<EOF > common/styles/index.scss
@import 'variables';
@import 'global';

@import 'hello-world.scss';
EOF

	cat <<EOF > common/styles/global.scss
* {
	box-sizing: border-box;
	margin: 0;
	outline: 0;
	padding: 0;
	-webkit-font-smoothing: antialiased;
	-moz-osx-font-smoothing: grayscale;

	p {
		margin: 0 !important;
	}
}
EOF

	cat <<EOF > common/styles/hello-world.scss
.hello-world {
	h1 {
		color: \$primary-color;
		font-weight: bold;
	}
}
EOF

}

