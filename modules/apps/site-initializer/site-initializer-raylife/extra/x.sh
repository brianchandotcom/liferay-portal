#!/bin/bash

function apply_craco {
	#
	# Install dependencies.
	#

	yarn add @craco/craco sass
	yarn add -D sass-to-string

	#
	# Use CRACO.
	#

	sed -i -e 's/"build": "react-scripts build"/"build": "craco build"/g' package.json
	sed -i -e 's/"start": "react-scripts start"/"start": "craco start"/g' package.json
	sed -i -e 's/"test": "react-scripts test"/"test": "craco test"/g' package.json

	write_craco_config_js

	write_gitignore

	write_index_html

	#
	# Remove temp files created by SED
	# Brian, for some reason, without that flag -e this sed command don't work for me
	#

	rm package.json-e

	#
	# Set up Yarn.
	#

	echo "SKIP_PREFLIGHT_CHECK=true" > ".env"
}

function check_usage {
	export CUSTOM_ELEMENT_NAME="hello-world"
}

function create_files {
	remove_unnecessary_files
	write_index_js
	write_routes_files
	write_common_files
}

function create_folders_structure {
	cd src

	#
	# Create all needed folders to write .js files
	#

	mkdir -p routes/hello-world/components routes/hello-world/pages
	mkdir -p common/services/liferay common/styles
}

function create_react_app {
	yarn create react-app ${1}
}

function date {
	export LC_ALL=en_US.UTF-8
	export TZ=America/Los_Angeles

	if [ -z ${1+x} ] || [ -z ${2+x} ]
	then
		if [ "$(uname)" == "Darwin" ]
		then
			echo $(/bin/date)
		elif [ -e /bin/date ]
		then
			echo $(/bin/date --iso-8601=seconds)
		else
			echo $(/usr/bin/date --iso-8601=seconds)
		fi
	else
		if [ "$(uname)" == "Darwin" ]
		then
			echo $(/bin/date -juf "%a %b %e %T %Z %Y" "${1}" "${2}")
		elif [ -e /bin/date ]
		then
			echo $(/bin/date -d "${1}" "${2}")
		else
			echo $(/usr/bin/date -d "${1}" "${2}")
		fi
	fi
}

