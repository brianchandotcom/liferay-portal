package com.liferay.web.hook.uad.anonymizer;

import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;
import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;
import com.liferay.web.hook.model.WebHookEntry;
import com.liferay.web.hook.service.WebHookEntryLocalService;
import com.liferay.web.hook.uad.constants.WebHookUADConstants;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the web hook entry UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link WebHookEntryUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseWebHookEntryUADAnonymizer
	extends DynamicQueryUADAnonymizer<WebHookEntry> {

	@Override
	public void autoAnonymize(
			WebHookEntry webHookEntry, long userId, User anonymousUser)
		throws PortalException {

		if (webHookEntry.getUserId() == userId) {
			webHookEntry.setUserId(anonymousUser.getUserId());
			webHookEntry.setUserName(anonymousUser.getFullName());
		}

		webHookEntryLocalService.updateWebHookEntry(webHookEntry);
	}

	@Override
	public void delete(WebHookEntry webHookEntry) throws PortalException {
		webHookEntryLocalService.deleteWebHookEntry(webHookEntry);
	}

	@Override
	public Class<WebHookEntry> getTypeClass() {
		return WebHookEntry.class;
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return webHookEntryLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return WebHookUADConstants.USER_ID_FIELD_NAMES_WEB_HOOK_ENTRY;
	}

	@Reference
	protected WebHookEntryLocalService webHookEntryLocalService;

}